package bo;

public class SpaceObject {
    private int oID;
    private int starName;
    private float magnitude;
    private int cID;
    
    public SpaceObject(int oID, int starName, float magnitude, int cID) {
        this.oID = oID;
        this.starName = starName;
        this.magnitude = magnitude;
        this.cID = cID;
    }
}
